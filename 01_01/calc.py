"""
四則演算を行うモジュール
"""
# Google Python Style 形式で記述

def add(a, b):
    """
    2つの引数を加算する
    
    Args:
        a: 加算する値
        b: 加算する値
    
    Returns:
        a と b の和
    """
    return a + b

def subtract(a, b):
    """
    2つの引数を減算する
    
    Args:
        a: 減算される値
        b: 減算する値
    
    Returns:
        a と b の差
    """
    return a - b

def multiply(a, b):
    """
    2つの引数を乗算する
    
    Args:
        a: 乗算する値
        b: 乗算する値
    
    Returns:
        a と b の積
    """
    return a * b

def divide(a, b):
    """
    2つの引数を除算する
    
    Args:
        a: 除算される値
        b: 除算する値
    
    Returns:
        a と b の商
    """
    return a / b